public class Realacional {
    // Relacional retorma valllores boleanos

    public static void main(String[] args) {
        if (2 == 2) {
            System.out.println("E igual");
        };

        if (5 > 3) {
            System.out.println("5 E maio que 3");
        }

        if (5 < 3) {
            System.out.println("5 E menor que 3");
        }

        if (4 >= 4) {
            System.out.println("4 E maio ou igual a 4");
        }

        if (4 <= 4) {
            System.out.println("4 E Menor ou igual a 4");
        }

        if (4 != 4) {
            System.out.println("4 E diferente de 4");
        }
    }
}
